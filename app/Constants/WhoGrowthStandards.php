    /** Ambil dataset berdasarkan indikator dan jenis kelamin (raw indexed array). */
    public static function get(string $indicator, string $sex): array
    {
        $key   = strtoupper($indicator . '_' . $sex);
        $const = constant("self::$key");
        if ($const === null) {
            throw new \InvalidArgumentException("Dataset tidak ditemukan: $key");
        }
        return $const;
    }

    /** Ambil dataset sebagai associative array — dipakai di Inertia props agar TypeScript bisa akses .month dll. */
    public static function getObjects(string $indicator, string $sex): array
    {
        return array_map(fn ($p) => [
            'month'  => $p[0],
            'sd3neg' => $p[1],
            'sd2neg' => $p[2],
            'sd0'    => $p[3],
            'sd2'    => $p[4],
            'sd3'    => $p[5],
        ], static::get($indicator, $sex));
    }
