/** Interpretasi z-score BB/Usia */
export function interpretWfa(z: number): string {
  if (z < -3) return 'Gizi Buruk';
  if (z < -2) return 'Gizi Kurang';
  if (z <= 2) return 'Gizi Baik';
  if (z <= 3) return 'Gizi Lebih';
  return 'Obesitas';
}

/** Interpretasi z-score TB/Usia */
export function interpretLhfa(z: number): string {
  if (z < -3) return 'Sangat Pendek';
  if (z < -2) return 'Pendek';
  if (z <= 3) return 'Normal';
  return 'Tinggi';
}

/** Interpretasi z-score LK/Usia */
export function interpretHcfa(z: number): string {
  if (z < -3) return 'Mikrosefali Berat';
  if (z < -2) return 'Mikrosefali';
  if (z <= 2) return 'Normal';
  if (z <= 3) return 'Makrosefali Ringan';
  return 'Makrosefali';
}