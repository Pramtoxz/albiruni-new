<ol class="petunjuk-list">
    <li>Buku Laporan Perkembangan Anak ini merupakan ringkasan hasil pengamatan guru terhadap tumbuh kembang dan capaian perkembangan anak selama satu semester.</li>
    <li>Laporan disajikan dalam bentuk deskripsi/narasi serta grafik pertumbuhan yang mengacu pada Standar Pertumbuhan Anak dari World Health Organization (WHO).</li>
    <li>Buku ini dipergunakan selama anak mengikuti pendidikan di Al-Biruni Preschool &amp; Daycare.</li>
    <li>Apabila anak pindah sekolah, buku ini dibawa oleh orang tua/wali sebagai dokumen perkembangan anak dengan meninggalkan arsip/salinan di sekolah asal.</li>
    <li>Apabila buku ini hilang, dapat diterbitkan kembali atas permintaan orang tua/wali dengan pengesahan Kepala Sekolah.</li>
    <li>Buku ini dilengkapi dengan pas foto anak ukuran 3×4 cm dan pengisiannya dilakukan oleh Guru Kelas.</li>
    <li>Penilaian aspek perkembangan menggunakan empat kategori capaian: BB (Belum Berkembang), MB (Mulai Berkembang), BSH (Berkembang Sesuai Harapan), dan BSB (Berkembang Sangat Baik).</li>
    <li>Grafik pertumbuhan (Berat Badan/Umur, Tinggi Badan/Umur, dan Lingkar Kepala/Umur) digunakan untuk memantau status gizi dan pertumbuhan anak. Garis biru menunjukkan data anak terhadap kurva standar WHO.</li>
    <li>Diharapkan kerja sama orang tua/wali untuk menindaklanjuti hasil laporan ini demi tumbuh kembang anak yang optimal.</li>
</ol>
